class ICD(object):

    def __init__(self, index, code, description):
        self.idx = index
        self.code = code
        self.description = description

    def get_idx(self):
        return self.idx

    def get_code(self):
        return self.code

    def get_description(self):
        return self.description




class ICDLabelLoader(object):

    def _load_labels(self, path):
        print("Loading Label Binarizer...")
        print(f"Loading ICD Code Binarizer, Reference Dataset Path: {path}")
        data = pd.read_csv(path, dtype={'LENGTH': int})
        return data['LABELS']

    def _load_description(self, path):
        desc_dict = dict()
        with open(path, 'r', encoding='utf8') as descfile:
            r = csv.reader(descfile)
            # header
            next(r)
            for row in r:
                code = row[0]
                desc = row[1]
                desc_dict[code] = desc
        return desc_dict

    def get_labels(self):
        return self.labels

    def __init__(self):
        GENERATED_DIR = constants.GENERATED_DIR
        dataset_path = GENERATED_DIR + '/train_full.csv'
        self.labels = set(self._load_labels(dataset_path))
        self.mlb = MultiLabelBinarizer()
        self.mlb.fit([list(self.labels)])
        self.description_dictionary = self._load_description(GENERATED_DIR + '/icd_description_zh.csv')

class ICDUtility(object):

    def __init__(self):
        self.icds = []
        for label in icd_label_set:
            index = numpy.where(icd_label_binarizer.transform([{label}])[0] == 1)[0][0]
            icd_label = ICD(index,
                            label,
                            icd_label_dict[label])
            self.icds.append(icd_label)

    def getICDByCode(self, code):
        for icd in self.icds:
            if icd.code == code:
                return icd
        return None

    def getICDByIndex(self, index):
        for icd in self.icds:
            if icd.idx == index:
                return icd
        return None

    def translateICD(self, code):
        icd = self.getICDByCode(code)
        if icd != None:
            return icd.description
        return None

    def getAllLabels(self):
        return icd_label_binarizer.classes_

    def getAllICDs(self):
        return self.icds

    def getIndexedICDDescriptions(self, description_max_len):
        result = []
        for i in range(len(icd_label_set)):
            description = self.getICDByIndex(i).description
            indexed_description = sentence_indexer.index(sentence_segmentor.segment_single(description), description_max_len)
            result.append(indexed_description)
        return result

